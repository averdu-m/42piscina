#include <stdio.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	*a;
	int	b;
	int	d;

	b = 0;
	while (b != (size / 2))
	{
		a = tab + b;
		d = *a;
		*a =*(tab + size - 1 - b);
		*(tab + size - 1 - b) = d;
		b++;
	}	
}
