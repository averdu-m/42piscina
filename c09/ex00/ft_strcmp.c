#include<string.h>
#include<unistd.h>
#include<stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	b;

	b = 0;
	while (*(s1 + b) != 0)
	{
		if (*(s1 + b) == *(s2 + b))
			b++;
		else
			return (*(s1 + b) - *(s2 + b));
	}
	return (0);
}
